
public class UpDown {
    public static void main(String[] args) {
        int i = 1;

        while(i <= 9){
            System.out.print(i + " ");
            i++;
        }
        System.out.println(i);

        for(int j = 10; j > 0; j--){
            System.out.print(j + " ");
        }
    }
}